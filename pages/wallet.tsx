import FormManager from "@/Components/Page/Common/FormManager";
import DataTable from "@/Components/UI/DataTable";
import Dropdown from "@/Components/UI/Dropdown";
import PopupModal from "@/Components/UI/PopupModal";
import TextBox from "@/Components/UI/TextBox";
import {
  WALLET_FETCH,
  WALLET_FUND_CREATE,
  WalletAction,
} from "@/Redux/Actions/WalletAction";
import { pageProps, rootReducer } from "@/utils/types";
import { Search } from "@mui/icons-material";
import {
  Box,
  Button,
  InputAdornment,
  Typography,
  useTheme,
} from "@mui/material";
import Head from "next/head";
import { useRouter } from "next/router";
import React, { useEffect, useState } from "react";
import { useDispatch, useSelector } from "react-redux";
import * as yup from "yup";

const columns = ["#", "Company Name", "Email", "Amount"];

const fundInitial = {
  amount: "",
  currency: "USD",
};

const Wallet = ({ setPageName }: pageProps) => {
  const theme = useTheme();
  const dispatch = useDispatch();
  const router = useRouter();
  const walletState = useSelector((state: rootReducer) => state.walletReducer);
  const [localData, setLocalData] = useState<any[]>([]);
  const [searchValue, setSearchValue] = useState("");
  const [open, setOpen] = useState(false);

  const fundSchema = yup.object().shape({
    amount: yup.string().required("amount is required!"),
  });

  useEffect(() => {
    setPageName("Wallets");
    dispatch(WalletAction(WALLET_FETCH));
  }, []);

  useEffect(() => {
    if (walletState.walletList.length > 0) {
      const tempArray: any[] = [];
      walletState.walletList.map((x, i) => {
        const tempObj = {
          no: i + 1,
          name: (
            <Box
              sx={{
                display: "flex",
                alignItems: "center",
                gap: 2,
              }}
            >
              {x.photo ? (
                <img
                  src={x.photo}
                  className="round-img"
                  crossOrigin="anonymous"
                />
              ) : (
                <Box
                  className="round-img"
                  sx={{
                    background: theme.palette.secondary.main + "44",
                    color: "text.secondary",
                    border: "3px solid",
                    display: "flex",
                    alignItems: "center",
                    justifyContent: "center",
                    lineHeight: 0,
                  }}
                >
                  {x.company_name.slice(0, 1)}
                </Box>
              )}
              {x.company_name}
            </Box>
          ),
          hidden: x.company_name,
          email: x.email,
          amount: "$" + x.amount.toFixed(2),
          id: x.wallet_id,
        };
        tempArray.push({ ...tempObj });
      });
      setLocalData(tempArray);
    }
  }, [walletState.walletList]);

  useEffect(() => {
    if (walletState.amount !== 0) {
      router.push("/payment");
    }
  }, [walletState.amount]);

  const handleSearchChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setSearchValue(e.target.value);
  };

  const handleSubmit = (values: any) => {
    dispatch({ type: WALLET_FUND_CREATE, payload: { ...values } });
  };
  return (
    <>
      <Head>
        <title>BozzWallet</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <PopupModal
        open={open}
        showClose
        handleClose={() => setOpen(false)}
        headerText={"Add Funds"}
      >
        <Box sx={{ minWidth: "450px" }}>
          <FormManager
            initialValues={fundInitial}
            yupSchema={fundSchema}
            onSubmit={handleSubmit}
          >
            {({
              errors,
              handleBlur,
              handleChange,
              submitDisable,
              touched,
              values,
            }) => (
              <>
                <TextBox
                  name="amount"
                  placeholder="Enter amount"
                  label="Amount"
                  value={values.amount}
                  type="number"
                  fullWidth
                  onChange={handleChange}
                  onBlur={handleBlur}
                  error={touched.amount && errors.amount}
                  helperText={touched.amount && errors.amount && errors.amount}
                />
                <Box sx={{ width: "100%", mt: 3 }}>
                  <Dropdown
                    name="currency"
                    placeholder="Enter currency"
                    label="Currency"
                    value={values.currency}
                    type="number"
                    fullWidth
                    menuItems={[
                      { label: "USD", value: "USD" },
                      { label: "NGN", value: "NGN" },
                    ]}
                    getValue={(value: any) => {
                      const e: any = {
                        target: {
                          name: "currency",
                          value,
                        },
                      };
                      handleChange(e);
                    }}
                    onBlur={handleBlur}
                    error={touched.currency && errors.currency}
                    helperText={
                      touched.currency && errors.currency && errors.currency
                    }
                  />
                </Box>
                <Box sx={{ mt: 5, mb: 3, display: "flex" }}>
                  <Button
                    variant="rounded"
                    disabled={walletState.loading ?? submitDisable}
                    type="submit"
                  >
                    Add
                  </Button>
                </Box>
              </>
            )}
          </FormManager>
        </Box>
      </PopupModal>
      <Box sx={{ m: 2, mb: 5 }}>
        <Box
          sx={{
            mt: 2,
            display: "flex",
            alignItems: "center",
            justifyContent: "space-between",
          }}
        >
          <TextBox
            customWidth="auto"
            placeholder="Search"
            value={searchValue}
            onChange={handleSearchChange}
            InputProps={{
              startAdornment: (
                <InputAdornment position="start">
                  <Search />
                </InputAdornment>
              ),
            }}
          />
          <Box sx={{ display: "flex", alignItems: "center", gap: 2 }}>
            <Box
              sx={{
                display: "flex",
                alignItems: "center",
                gap: 1,
                "& p": {
                  fontWeight: 700,
                },
              }}
            >
              <Typography color={"text.secondary"}>Total Balance:</Typography>
              <Typography>${walletState.totalBalance.toFixed(2)}</Typography>
            </Box>
            <Button variant="rounded" color="secondary">
              Withdraw
            </Button>
            <Button
              variant="rounded"
              color="primary"
              onClick={() => {
                setOpen(true);
              }}
            >
              Add Funds
            </Button>
          </Box>
        </Box>
        <Box>
          <DataTable
            columns={columns}
            data={localData}
            hasAction
            actionText="Action"
            searchValue={searchValue}
            actionColumn={(index) => (
              <Box
                sx={{
                  "& button": {
                    mr: 1,
                  },
                }}
              >
                <Button
                  variant="rounded"
                  disableRipple={false}
                  sx={{ borderRadius: "20px" }}
                  onClick={() => {
                    const id = localData[index].id;
                    alert(id);
                  }}
                >
                  Transactions
                </Button>
              </Box>
            )}
          />
        </Box>
      </Box>
    </>
  );
};

export default Wallet;
